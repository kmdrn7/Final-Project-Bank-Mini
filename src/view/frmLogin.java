    private void BtnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSubmitActionPerformed
        switch (typeTransaksi) {
            case "Teller":
                teller = new TellerController(db);
                if (teller.login(fieldUsername.getText(), fieldPassword.getText())) {
                    title = "Selamat Datang di Dashboard Teller, " + teller.getNama();
                    transaksiV = new frmTransaksi(title, typeTransaksi, db, teller, main);
                    transaksiV.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "No Rekening / Password Salah");
                }

                break;
            case "ATM":
                atm = new ATMController(db);
                if (atm.login(fieldUsername.getText(), fieldPassword.getText())) {
                    title = "Selamat Datang di Dashboard ATM, " + atm.getNama();
                    transaksiV = new frmTransaksi(title, typeTransaksi, db, atm, main);
                    transaksiV.btnSetor.setVisible(false);
                    transaksiV.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "No ATM / Password anda salah");
                }
                break;
            default:
                online = new OnlineController(db);
                if (online.login(fieldUsername.getText(), fieldPassword.getText())) {
                    title = "Selamat Datang di Dashboard Online, " + online.getNama();
                    transaksiV = new frmTransaksi(title, typeTransaksi, db, online, main);
                    transaksiV.btnSetor.setVisible(false);
                    transaksiV.btnTarik.setVisible(false);
                    transaksiV.setVisible(true);
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(null, "Username / Password anda salah");
                }

                break;
        }

    }//GEN-LAST:event_BtnSubmitActionPerformed

